#!/usr/bin/env python3
# Function 1: Greet the programmer
def greet_programmer():
    print("Hello, programmer!")

# Function 2: Greet a specific person by name
def greet(name):
    print(f"Hello, {name}!")

# Function 3: Greet a specific person or use a default name if none is provided
def greet_with_default(name="programmer"):
    print(f"Hello, {name}!")

# Function 4: Add two numbers and return the sum
def add(num1, num2):
    return num1 + num2

# Function 5: Halve a number by dividing it by 2
def halve(number):
    return number / 2